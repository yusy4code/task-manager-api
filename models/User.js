const mongoose = require("mongoose");

const UserSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});
// model method 3rd arg is optional,
// moongoose will automatically look for plural name of the model for collection name
module.exports = mongoose.model("User", UserSchema, "users");
